
package resturant;



public class G {
    
    
    private final String name_mng;
    private final String name_eg;
    private final String g;
    private final String d;
    private final int buy;
    private final int change;
    
    public G(String name_mng, String name_eg, String g, String d, int buy, int change) {
        this.name_mng = name_mng;
        this.name_eg = name_eg;
        this.g = g;
        this.d = d;
        this.buy = buy;
        this.change = change;
    }

    public String getName_mng() {
        return name_mng;
    }

    public String getName_eg() {
        return name_eg;
    }

    public String getG() {
        return g;
    }

    public String getD() {
        return d;
    }

    public int getBuy() {
        return buy;
    }

    public int getChange() {
        return change;
    }
   
    
   
    
}
